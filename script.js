// ── Constructor de Letras (Ahora son LINKS <a>) ──
function buildLetter({ ch, color, kid, replace, id }, delay) {
  const link = document.createElement('a'); // Ahora son enlaces
  link.href = "#"; // Aquí pones tu destino final
  link.className = `letter letter-out`;
  link.id = id;
  link.style.color = color;

  if (replace) {
    link.classList.add('letter-svg-only');
    link.innerHTML = svgs[kid];
  } else {
    link.textContent = ch;
    if (kid) {
      const wrap = document.createElement('span');
      wrap.className = 'kid-wrap';
      wrap.innerHTML = svgs[kid];
      link.appendChild(wrap);
    }
  }

  setTimeout(() => link.classList.replace('letter-out', 'letter-in'), delay);
  return link;
}

